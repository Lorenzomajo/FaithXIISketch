public class Religion {

    String rName = null;
    int rPower;
    int rKnowledge;
    int rLegacy;

    Religion(String rName, int rPower, int rKnowledge, int rLegacy){
        this.rName = rName;
        this.rPower = rPower;
        this.rKnowledge = rKnowledge;
        this.rLegacy = rLegacy;
    }

}
